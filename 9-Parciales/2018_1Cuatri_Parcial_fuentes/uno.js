
function mostrar()
{
    let ancho;
    let largo;
    let perimetro;

    ancho = parseInt(prompt("Ingrese el ancho del terreno por favor: "));

    largo = parseInt(prompt("Ingrese el largo del terreno por favor: "));

    perimetro = 2 * (largo + ancho);

    alert("El perimetro del rectangulo es " + perimetro + " mt2")





}
